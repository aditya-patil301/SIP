// Undefined - Declared but not Initialized
let a;
console.log(a);
console.log(typeof(a));

// Null - Declared and Initialized with null
let b = null;
console.log(b);
console.log(typeof(b));

// typeOf() method - Used to check Data Type
let c = "7";
let d = 7;
console.log(typeof(c));
console.log(typeof(d));

// TypeCasting
let z = Number(c);
let y = String(d);
console.log(typeof(z));
console.log(typeof(y));